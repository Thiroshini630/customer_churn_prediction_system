import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score, classification_report

# Generate dummy customer dataset
np.random.seed(42)

data = pd.DataFrame({
    'tenure': np.random.randint(1, 72, 500),
    'monthly_charges': np.random.uniform(20, 120, 500),
    'support_calls': np.random.randint(0, 10, 500),
    'churn': np.random.randint(0, 2, 500)
})

X = data[['tenure', 'monthly_charges', 'support_calls']]
y = data['churn']

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.2)

model = LogisticRegression()
model.fit(X_train, y_train)

predictions = model.predict(X_test)

print("Accuracy:", accuracy_score(y_test, predictions))
print("\nClassification Report:\n")
print(classification_report(y_test, predictions))